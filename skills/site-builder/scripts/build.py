# ── Entry Point ───────────────────────────────────────────────────────────────
def main():
    parser = argparse.ArgumentParser(description="site-builder — Next.js site generator")
    sub = parser.add_subparsers(dest="command")

    # new
    p_new = sub.add_parser("new", help="Scaffold a new project")
    p_new.add_argument("project_dir", nargs="?")
    p_new.add_argument("--vertical", help="Vertical key (e.g. dental, saas)")
    p_new.add_argument("--brand", help="Brand colors: '#hex1,#hex2'")
    p_new.add_argument("--name", help="Project/client name")
    p_new.add_argument("--domain", help="Domain name")
    p_new.add_argument("--dry-run", action="store_true")
    p_new.add_argument("--yes", "-y", action="store_true", help="Skip confirmations")

    # verticals
    sub.add_parser("verticals", help="List all available verticals")

    # design
    p_design = sub.add_parser("design", help="Generate design system only")
    p_design.add_argument("--vertical")
    p_design.add_argument("--brand")
    p_design.add_argument("--name")
    p_design.add_argument("--domain")

    # audit
    p_audit = sub.add_parser("audit", help="Audit an existing project")
    p_audit.add_argument("project_dir", nargs="?")

    args = parser.parse_args()

    if args.command == "new":
        cmd_new(args)
    elif args.command == "verticals":
        cmd_verticals(args)
    elif args.command == "design":
        cmd_design(args)
    elif args.command == "audit":
        cmd_audit(args)
    else:
        parser.print_help()

if __name__ == "__main__":
    main()
