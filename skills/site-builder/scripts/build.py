# ── Project Scaffold ──────────────────────────────────────────────────────────
def scaffold_project(project_dir: Path, vertical_key: str, vertical: dict,
                     project_name: str, domain: str, brand_colors: list, dry_run: bool):

    header("SCAFFOLDING PROJECT")

    if project_dir.exists() and any(project_dir.iterdir()):
        if not dry_run:
            answer = ask(f"{project_dir} already exists and has files. Overwrite?", "n")
            if answer.lower() != "y":
                err("Cancelled")
                sys.exit(1)

    # Check for base template
    base_template = TEMPLATES_DIR / "base"
    if base_template.exists():
        if not dry_run:
            if project_dir.exists():
                shutil.rmtree(project_dir)
            shutil.copytree(base_template, project_dir)
            ok(f"Copied base template to {project_dir}")
        else:
            info(f"[dry-run] Would copy base template → {project_dir}")
    else:
        # No template yet — use create-next-app
        if not dry_run:
            info("No base template found — scaffolding with create-next-app...")
            result = subprocess.run(
                f"npx create-next-app@latest {project_dir} --typescript --tailwind --app --no-src-dir --no-import-alias --yes",
                shell=True, capture_output=True, text=True
            )
            if result.returncode != 0:
                err(f"create-next-app failed: {result.stderr[:300]}")
                sys.exit(1)
            ok("Next.js project created")
        else:
            info(f"[dry-run] Would run create-next-app → {project_dir}")

    if dry_run:
        return

    # Write design system
    ds_dir = project_dir / "design-system"
    ds_dir.mkdir(exist_ok=True)
    ds_content = generate_design_system(vertical_key, vertical, brand_colors, project_name, domain)
    (ds_dir / "MASTER.md").write_text(ds_content)
    ok("design-system/MASTER.md written")

    # Write AGENT_TASK.md
    task_content = generate_agent_task(vertical_key, vertical, project_name, domain, project_dir)
    (project_dir / "AGENT_TASK.md").write_text(task_content)
    ok("AGENT_TASK.md written")

    # Write .deploy.json
    deploy_config = {
        "env_file": ".env.local",
        "asset_min_kb": 200,
        "hero_patterns": ["hero", "step", "about", "feature", "banner"],
        "smoke_urls": [
            {"path": "/", "expect": 200},
            {"path": "/sitemap.xml", "expect": 200},
            {"path": "/robots.txt", "expect": 200}
        ]
    }
    (project_dir / ".deploy.json").write_text(json.dumps(deploy_config, indent=2))
    ok(".deploy.json written")

    # Write .env.local stub
    env_stub = f"""# {project_name} — Environment Variables
# Fill these in before deploying

NEXT_PUBLIC_SUPABASE_URL=
NEXT_PUBLIC_SUPABASE_ANON_KEY=
SUPABASE_SERVICE_KEY=

# Dashboard auth (if using /dashboard)
DASHBOARD_PASSWORD=
DASHBOARD_JWT_SECRET=

# Google Places (if using address autocomplete)
NEXT_PUBLIC_GOOGLE_PLACES_API_KEY=

# Forms (if using Formspree)
NEXT_PUBLIC_FORMSPREE_ID=
"""
    env_path = project_dir / ".env.local"
    if not env_path.exists():
        env_path.write_text(env_stub)
        ok(".env.local stub created")

    # Inject brand colors into globals.css if it exists
    globals_css = project_dir / "src/app/globals.css"
    if not globals_css.exists():
        globals_css = project_dir / "app/globals.css"

    if globals_css.exists():
        primary = brand_colors[0] if brand_colors else vertical["colors"]["primary"]
        accent = brand_colors[1] if len(brand_colors) > 1 else vertical["colors"]["accent"]
        color_vars = f"""
/* Site-builder: Brand CSS Variables */
:root {{
  --color-primary: {primary};
  --color-accent: {accent};
  --color-bg: {vertical['colors']['bg']};
  --color-surface: {vertical['colors']['surface']};
  --color-text: {vertical['colors']['text']};
}}
"""
        existing = globals_css.read_text()
        if "--color-primary" not in existing:
            globals_css.write_text(existing + color_vars)
            ok("Brand CSS variables injected into globals.css")

    # Generate hero image
    info(f"Hero image prompt: {vertical['hero_image'][:60]}...")
    hero_assets_dir = project_dir / "public/assets"
    hero_assets_dir.mkdir(parents=True, exist_ok=True)
    image_gen = Path.home() / ".openclaw/skills/image-gen/scripts/generate.py"
    if image_gen.exists():
        info("Generating hero image with gpt-image-1.5 (background)...")
        subprocess.Popen(
            f"source ~/.openclaw/.env && uv run {image_gen} "
            f'--prompt "{vertical["hero_image"]}" '
            f"--provider openai --model best --aspect-ratio 16:9 "
            f"--filename {hero_assets_dir}/hero.png",
            shell=True, executable="/bin/zsh",
            stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL
        )
        info("Hero image generating in background (~30s)")
    else:
        warn("image-gen skill not found — generate hero image manually")

    # Initialize git repo and make initial commit
    if HAS_GIT_UTILS:
        ensure_git_repo(project_dir)
        # Make initial commit of scaffold
        import subprocess as _sp
        _sp.run("git add -A", shell=True, cwd=project_dir, capture_output=True)
        r = _sp.run('git commit -m "Initial scaffold (site-builder)"',
                    shell=True, cwd=project_dir, capture_output=True)
        if r.returncode == 0:
            ok("Initial git commit created — ready for worktree-based development")

# ── Commands ──────────────────────────────────────────────────────────────────
def cmd_verticals(args):
    verticals = load_verticals()
    header(f"AVAILABLE VERTICALS ({len(verticals)})")
    rule()
    for key, v in verticals.items():
        print(f"  {BOLD}{key:<25}{R}  {DIM}{v['name']}{R}")
        print(f"  {'':25}  {DIM}Style: {v['style']} · Pattern: {v['pattern']}{R}")
        print()

def cmd_design(args):
    verticals = load_verticals()
    vertical_key = args.vertical or pick_vertical(verticals)
    vertical = verticals[vertical_key]
    brand_colors = [c.strip() for c in args.brand.split(",")] if args.brand else []
    project_name = args.name or vertical["name"]
    domain = args.domain or "example.com"

    header(f"DESIGN SYSTEM — {project_name}")
    ds = generate_design_system(vertical_key, vertical, brand_colors, project_name, domain)
    print(ds)

def cmd_new(args):
    if not args.project_dir:
        err("Provide a project directory: site-builder new ./my-project")
        sys.exit(1)

    verticals = load_verticals()
    project_dir = Path(args.project_dir).resolve()

    print(f"\n{BOLD}{'━'*60}{R}")
    print(f"{BOLD}  🏗️  site-builder — new project{R}")
    if args.dry_run:
        print(f"  {YLW}DRY RUN — nothing will be executed{R}")
    print(f"{BOLD}{'━'*60}{R}")

    # Collect inputs
    vertical_key = args.vertical or pick_vertical(verticals)
    vertical = verticals[vertical_key]

    brand_input = args.brand or ask("Brand colors (hex, comma-separated)", f"{vertical['colors']['primary']},{vertical['colors']['accent']}")
    brand_colors = [c.strip() for c in brand_input.split(",") if c.strip()]

    project_name = args.name or ask("Project/client name", vertical["name"])
    domain = args.domain or ask("Domain", "example.com")

    # Summary
    header("PLAN")
    rule()
    print(f"  {DIM}Project:{R}    {project_dir}")
    print(f"  {DIM}Vertical:{R}   {vertical['name']}")
    print(f"  {DIM}Style:{R}      {vertical['style']}")
    print(f"  {DIM}Fonts:{R}      {vertical['fonts']['display']} / {vertical['fonts']['body']}")
    print(f"  {DIM}Colors:{R}     {' · '.join(brand_colors)}")
    print(f"  {DIM}Sections:{R}   {', '.join(vertical['sections'][:4])}...")
    print(f"  {DIM}shadcn:{R}     {', '.join(vertical['shadcn'][:4])}...")
    rule()
    print()

    if not args.dry_run and not args.yes:
        confirm = ask("Build this project?", "y")
        if confirm.lower() != "y":
            err("Cancelled")
            sys.exit(0)

    scaffold_project(project_dir, vertical_key, vertical, project_name, domain, brand_colors, args.dry_run)

    if not args.dry_run:
        header("NEXT STEPS")
        rule()
        ok(f"Project ready at {project_dir}")
        print()
        info("1. Fill in .env.local with your API keys")
        info("2. Hand to Claude Code:")
        print(f"\n  {DIM}cd {project_dir}{R}")
        print(f"  {DIM}claude --permission-mode bypassPermissions --print \"Read AGENT_TASK.md and execute it\"{R}")
        print()
        info("3. Deploy when done:")
        print(f"  {DIM}uv run ~/.openclaw/skills/nextjs-deploy/scripts/deploy.py {project_dir}{R}")
        rule()
        print()

